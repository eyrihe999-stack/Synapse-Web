/** 通用兜底提示 */
const FALLBACK_MESSAGES: Record<number, string> = {
  400: '请求参数有误',
  401: '请重新登录',
  403: '没有操作权限',
  404: '请求的资源不存在',
  409: '数据冲突，请检查后重试',
  429: '请求过于频繁，请稍后再试',
  500: '服务器内部错误，请稍后重试',
};

/**
 * 根据 API 响应提取用户友好的中文错误提示。
 *
 * 优先使用业务错误码映射，其次使用 HTTP 状态码兜底，最后返回通用提示。
 */
export function resolveErrorMessage(
  responseData?: { code?: number; message?: string; error?: string },
  httpStatus?: number,
): string {
  // 1. 尝试从业务错误码获取中文提示
  if (responseData?.code) {
    const msg = ERROR_MESSAGES[responseData.code];
    if (msg) return msg;
  }

  // 2. 用 HTTP 状态码兜底
  if (httpStatus) {
    const msg = FALLBACK_MESSAGES[httpStatus];
    if (msg) return msg;
  }

  // 3. 用后端返回的 error 或 message 字段
  if (responseData?.error) return responseData.error;
  if (responseData?.message && responseData.message !== 'ok') return responseData.message;

  return '操作失败，请稍后重试';
}
